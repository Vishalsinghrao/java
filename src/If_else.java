public class If_else {
    public static void main(String[] args) {
        int a = 10;
        // if (a=11){ this will throw an error cause we used assignment operator instead of not to check equality
        if(a==11){
            System.out.println("i am 11");
        }
        else{
            System.out.println("i am not 11");
        }
    }
}
